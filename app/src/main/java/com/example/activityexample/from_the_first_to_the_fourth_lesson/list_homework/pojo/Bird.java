package com.example.activityexample.from_the_first_to_the_fourth_lesson.list_homework.pojo;

public class Bird {
    private String name;
    private int age;
    private double height;

    public Bird(String name, int age, double height) {
        this.name = name;
        this.age = age;
        this.height = height;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public double getHeight() {
        return height;
    }

    public void setHeight(double height) {
        this.height = height;
    }
}
